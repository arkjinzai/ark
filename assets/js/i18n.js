let currentLang = 'jp';

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  
  // Save preference
  localStorage.setItem('preferredLang', lang);

  // Apply translations
  const dict = translations[lang];
  for (const selector in dict) {
    const el = document.querySelector(selector);
    if (el) {
      el.innerHTML = dict[selector];
    }
  }

  // Update button text
  const langBtn = document.querySelector('.lang-btn');
  if (langBtn) {
    const langLabels = { id: 'ID', en: 'EN', jp: 'JP' };
    langBtn.innerHTML = langLabels[lang] + ' <span>▼</span>';
  }

  // Update document language
  document.documentElement.lang = lang;

  // Change font based on language
  if (lang === 'jp') {
    document.body.style.fontFamily = "'Noto Sans JP', sans-serif";
  } else {
    document.body.style.fontFamily = "'Poppins', 'Noto Sans JP', sans-serif";
  }
}

function toggleLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) {
    menu.classList.toggle('show');
  }
}

// Close menu when clicking outside
document.addEventListener('click', function(e) {
  const switcher = document.querySelector('.lang-switcher');
  const menu = document.getElementById('langMenu');
  if (switcher && menu && !switcher.contains(e.target)) {
    menu.classList.remove('show');
  }
});

// Initialize on load
const savedLang = localStorage.getItem('preferredLang') || 'jp';
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => setLanguage(savedLang));
} else {
  setLanguage(savedLang);
}
